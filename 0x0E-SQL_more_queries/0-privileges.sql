-- This script lists all privileges of the MySQL users user_0d_1 and user_0d_2 on your server (in localhost)

-- Use the SHOW GRANTS command to display the privileges granted to a MySQL user
SHOW GRANTS FOR 'user_0d_1'@'localhost';
-- The above command will list all the privileges of the user 'user_0d_1' on 'localhost'

SHOW GRANTS FOR 'user_0d_2'@'localhost';
-- The above command will list all the privileges of the user 'user_0d_2' on 'localhost'
